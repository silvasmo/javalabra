
package logiikka;

public class Sanapari {
    private String sana1;
    private String sana2;
    
    public Sanapari(String sana1, String sana2) {
        this.sana1 = sana1;
        this.sana2 = sana2;
    }
    
    
    public String getSana1() {
        return this.sana1;
    }
    
    public String getSana2() {
        return this.sana2;
    }
    
}
